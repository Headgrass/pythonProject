'''
Сортировка пузырьком
Начальный массив: [3, 1, 5, 0, 7, 9, 8]
1.[1, 3, 5, 0, 7, 9, 8]
2.[1, 3, 0, 5, 7, 9, 8]
3.[1, 0, 3, 5, 7, 9, 8]
4.[0, 1, 3, 5, 7, 9, 8]
5.[0, 1, 3, 5, 7, 8, 9]
'''

arr = [9, 6, 1, 8, 5, 7, 2, 4, 3, 10]
print(f"Начальный массив: {arr}")
for i in range(len(arr)):
    for j in range(len(arr) - 1):
        if arr[j] > arr[j + 1]:
            temp = arr[j]
            arr[j] = arr[j + 1]
            arr[j + 1] = temp

    print(f"{i}  {arr}")


